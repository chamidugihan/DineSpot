    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 1){
            int selectedRow = jTable1.getSelectedRow();

            if (selectedRow != -1) {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

                String id = String.valueOf(model.getValueAt(selectedRow, 0));
                String name = String.valueOf(model.getValueAt(selectedRow, 1));
                String qty = String.valueOf(model.getValueAt(selectedRow, 2));
                
                this.id = id;

                jTextField1.setText(name);
                jTextField2.setText(qty);
                jFormattedTextField3.setEnabled(true);
                
            }
        }else if (evt.getClickCount() == 2){
            int selectedRow = jTable1.getSelectedRow();

            if (selectedRow != -1) {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

                String id = String.valueOf(model.getValueAt(selectedRow, 0));

                inventoryStatus ut = new inventoryStatus(adminHome, true, id);
                ut.setVisible(true);

                reset();
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        Double qty = Double.parseDouble(jTextField2.getText()) ;
        String usage = jFormattedTextField3.getText();
        
        if(usage.isEmpty()){
             JOptionPane.showMessageDialog(this, "Please Enter Usage", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(usage.matches("^[a-zA-Z]*$")){
        JOptionPane.showMessageDialog(this, "Please Enter Valid Usage", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            try {
               Double uqty = Double.parseDouble(usage);
               if(uqty>qty){
                   
                   JOptionPane.showMessageDialog(this, "Not enough Stock Available", "Warning", JOptionPane.WARNING_MESSAGE);
               }else{
                   Double newqty = qty - uqty;
                   MySQL.execute("UPDATE `inventory` SET `qty(kg)`='"+newqty+"' WHERE `id`='"+id+"' ");
                   reset();
               }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

