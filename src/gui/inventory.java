    public void loadInventory() {
        try {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet resultset = MySQL.execute("SELECT * FROM `inventory` "
                    
                    + "INNER JOIN `status` ON `inventory`.`status_id` = `status`.`id`");

            while (resultset.next()) {
                String id = resultset.getString("id");
                String name = resultset.getString("name");
                String qty = resultset.getString("qty(kg)");
                
                String status = resultset.getString("status.status");

                Vector vector = new Vector(); // Row
                vector.add(id);
                vector.add(name);
                vector.add(qty);
                vector.add(status);
                

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    public void setupSearchFunctionality() {
        jTextField5.setColumns(20);

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        jTable1.setRowSorter(sorter);

        jTextField5.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent e) {
                String searchText = jTextField5.getText().toLowerCase();
                if (searchText.trim().isEmpty()) {
                    sorter.setRowFilter(null); // Reset filter if search field is empty
                } else {
                    sorter.setRowFilter(RowFilter.regexFilter("(?i)^" + searchText, 1)); // Apply to the email column (index 3)
                }
            }
        });
    }

